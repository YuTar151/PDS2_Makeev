
# 2. Напишіть функцію, яка приймає довільну кількість словників, збирає їх в один словник та повертає його.

def my_function(my_dict):
    new_dict = {}
    for i in my_dict:
        new_dict |= i

    return new_dict

my_dict = ({1: 'січень', 2: 'лютий', 3: 'березень'}, {4: 'квітень', 5: 'травень', 6: 'червень'}, {7: 'липень', 8: 'серпень', 9: 'вересень'}, {10: 'жовтень', 11: 'листопад', 12: 'грудень'})

my_function(my_dict)

print(my_function(my_dict))